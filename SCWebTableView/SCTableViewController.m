#pragma mark - Table view data source

- (NSInteger)numberOfSectionsInTableView:(UITableView *)tableView
{
    // Return the number of sections.
    return 1;
}

- (NSInteger)tableView:(UITableView *)tableView numberOfRowsInSection:(NSInteger)section
{
    // Return the number of rows in the section.
    return [self.SCKDataArrayFromAFNetworking count];
}


- (UITableViewCell *)tableView:(UITableView *)tableView cellForRowAtIndexPath:(NSIndexPath *)indexPath
{
    static NSString *CellIdentifier = @"Cell";
    UITableViewCell *cell = [tableView dequeueReusableCellWithIdentifier:CellIdentifier forIndexPath:indexPath];

    // Configure the cell...
    NSDictionary *tempDictionary= [self.SCKDataArrayFromAFNetworking objectAtIndex:indexPath.row];
    cell.textLabel.text = [tempDictionary valueForKeyPath:@"properties.title"];
    cell.detailTextLabel.text = [tempDictionary valueForKeyPath:@"properties.location"];
    return cell;
}


-(void)makeSCApiRequest
{
    NSURL *url = [NSURL URLWithString:@"http://smartcitizen.me/devices/all.geojson"];
    
    NSURLRequest *request = [NSURLRequest requestWithURL:url];
    //AFNetworking asynchronous url request
    AFHTTPRequestOperation *operation = [[AFHTTPRequestOperation alloc] initWithRequest:request];
    
    operation.responseSerializer = [AFJSONResponseSerializer serializer];
    [operation setCompletionBlockWithSuccess:^(AFHTTPRequestOperation *operation, id responseObject) {
        
        self.SCKDataArrayFromAFNetworking = [responseObject objectForKey:@"features"];
        
        // Reload Data to display on view
        [self.tableView reloadData];
        
    } failure:^(AFHTTPRequestOperation *operation, NSError *error) {
        
        NSLog(@"Request Failed: %@, %@", error, error.userInfo);
        
    }];
    
    [operation start];
    
}






